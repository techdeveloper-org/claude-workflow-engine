def _iter_methods(call_graph_snapshot):
    """Yield method node dicts from either a CallGraph instance or a snapshot dict."""
    if hasattr(call_graph_snapshot, "methods"):
        # CallGraph instance: .methods is a dict FQN -> node
        yield from call_graph_snapshot.methods.values()
        return

    # Dict snapshot: may have 'nodes' as list or as {'methods': [...], 'classes': [...]}
    if isinstance(call_graph_snapshot, dict):
        nodes = call_graph_snapshot.get("nodes", [])
        if isinstance(nodes, list):
            for n in nodes:
                if n.get("type") in ("method", "function"):
                    yield n
        elif isinstance(nodes, dict):
            for n in nodes.get("methods", []):
                yield n
    return


def _get_call_paths(call_graph_snapshot):
    """Return call paths from a CallGraph instance or snapshot dict."""
    if hasattr(call_graph_snapshot, "compute_call_paths"):
        return call_graph_snapshot.compute_call_paths() or []

    if isinstance(call_graph_snapshot, dict):
        return call_graph_snapshot.get("call_paths", []) or []

    return []


def _build_methods_lookup(call_graph_snapshot):
    """Build a FQN -> method node dict for param lookups."""
    result = {}
    try:
        if hasattr(call_graph_snapshot, "methods"):
            return dict(call_graph_snapshot.methods)
        for node in _iter_methods(call_graph_snapshot):
            fqn = node.get("id", "")
            if fqn:
                result[fqn] = node
    except Exception:
        pass
    return result


def _normalize_file_set(modified_files):
    """Normalize modified file paths to a set of forward-slash strings."""
    normalized = set()
    try:
        for f in modified_files or []:
            s = str(f).replace("\\", "/")
            normalized.add(s)
            # Also add just the basename for loose matching
            normalized.add(Path(s).name)
    except Exception:
        pass
    return normalized


def _fqn_belongs_to_files(fqn, file_set):
    """Check whether a FQN's file portion matches any entry in file_set."""
    if not fqn or "::" not in fqn:
        return False
    fqn_file = fqn.split("::")[0].replace("\\", "/")
    for f in file_set:
        if fqn_file == f or fqn_file.endswith("/" + f) or f.endswith("/" + fqn_file):
            return True
        # Match by basename alone
        if Path(fqn_file).name == f or Path(fqn_file).name == Path(f).name:
            return True
    return False


def _resolve_test_file_path(project_root, modified_files):
    """Determine the integration test file path based on modified files.

    Convention: tests/integration/test_integration_<stem>.py
    If multiple files are modified, uses 'modified' as the stem.
    """
    try:
        files = [str(f) for f in (modified_files or []) if f]
        if len(files) == 1:
            stem = Path(files[0]).stem
        elif files:
            stem = "modified"
        else:
            stem = "integration"

        return str(project_root / "tests" / "integration" / ("test_integration_%s.py" % stem))
    except Exception:
        return str(project_root / "tests" / "integration" / "test_integration.py")
