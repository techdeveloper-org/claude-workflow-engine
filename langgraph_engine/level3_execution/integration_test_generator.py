def generate_path_test(path, methods_data):
    """Generate a single integration test class for one call path.

    Args:
        path:         Path dict: {"id": str, "path": [fqn, ...],
                                  "depth": int, "total_complexity": int}
        methods_data: Dict of FQN -> method node dict (from call graph).
                      Used to look up params, class names etc.

    Returns:
        str: Python test class source code.
    """
    try:
        path_id = _safe_identifier(path.get("id", "path_0"))
        node_fqns = path.get("path", [])

        if not node_fqns:
            return ""

        entry_fqn = node_fqns[0]
        entry_module, entry_class, entry_method = _fqn_to_module_and_class(entry_fqn)

        # Build human-readable path label for docstring
        labels = []
        for fqn in node_fqns[:5]:  # limit label length
            _, cls, mth = _fqn_to_module_and_class(fqn)
            if cls:
                labels.append("%s.%s" % (cls, mth))
            else:
                labels.append(mth)
        ellipsis = " -> ..." if len(node_fqns) > 5 else ""
        doc = "Tests call path: %s%s" % (" -> ".join(labels), ellipsis)

        # Lookup entry method params
        entry_node = methods_data.get(entry_fqn, {})
        entry_params = entry_node.get("params", []) if entry_node else []
        test_input = _default_test_input(entry_params)

        lines = []
        lines.append(_INT_CLASS_HEADER.format(path_id=path_id, doc=doc))

        # --- test_full_path_executes ---
        if entry_class:
            lines.append(
                _INT_FULL_PATH_TEST.format(
                    entry_import="%s.%s" % (entry_module, entry_class) if entry_module else entry_class,
                    module=entry_module or entry_class,
                    class_name=entry_class,
                    entry_method=entry_method,
                    test_input=test_input,
                )
            )
        else:
            lines.append(
                _INT_FULL_PATH_TEST_NO_CLASS.format(
                    entry_import="%s.%s" % (entry_module, entry_method) if entry_module else entry_method,
                    module=entry_module or "__main__",
                    entry_method=entry_method,
                    test_input=test_input,
                )
            )

        # --- test_intermediate_called_* for nodes 1..N-1 ---
        for inter_fqn in node_fqns[1:]:
            inter_module, inter_class, inter_method = _fqn_to_module_and_class(inter_fqn)
            safe_mth = _safe_identifier(inter_method)
            patch_target = _build_patch_target(inter_module, inter_class, inter_method)

            if entry_class:
                lines.append(
                    _INT_INTERMEDIATE_TEST.format(
                        safe_method=safe_mth,
                        intermediate_fqn=inter_fqn,
                        patch_target=patch_target,
                        module=entry_module or entry_class,
                        class_name=entry_class,
                        entry_method=entry_method,
                        test_input=test_input,
                    )
                )
            else:
                lines.append(
                    _INT_INTERMEDIATE_TEST_NO_CLASS.format(
                        safe_method=safe_mth,
                        intermediate_fqn=inter_fqn,
                        patch_target=patch_target,
                        module=entry_module or "__main__",
                        entry_method=entry_method,
                        test_input=test_input,
                    )
                )

        return "".join(lines)

    except Exception:
        return ""


