# 6. write_integration_tests
# ---------------------------------------------------------------------------


def write_integration_tests(project_root, test_result, dry_run=False):
    """Write or preview the generated integration test file.

    Args:
        project_root: Root directory of the project (str or Path).
        test_result:  Dict returned by generate_integration_tests().
        dry_run:      If True, returns the path without writing to disk.

    Returns:
        str: Path of the file written (or that would be written), or ""
             if there was nothing to write.
    """
    try:
        test_file_path = test_result.get("test_file_path", "")
        test_code = test_result.get("test_code", "")

        if not test_file_path or not test_code.strip():
            return ""

        if not dry_run:
            dest = Path(str(test_file_path))
            dest.parent.mkdir(parents=True, exist_ok=True)
            dest.write_text(test_code, encoding="utf-8")

        return test_file_path

    except Exception:
        return ""


