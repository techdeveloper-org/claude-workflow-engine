# 1. detect_api_endpoints
# ---------------------------------------------------------------------------


def detect_api_endpoints(call_graph_snapshot):
    """Find method FQNs that look like HTTP API endpoints.

    Heuristics applied in order:
    1. Method name contains any of: get, post, put, delete, patch, handle, route
    2. Parent class name contains any of: View, Controller, Handler, Resource, Router
    3. Method has 'request' in its param list

    Args:
        call_graph_snapshot: A CallGraph instance (has .methods dict) or a dict
                             with a 'nodes' key (list of node dicts).

    Returns:
        list of str: Method FQNs that appear to be API endpoints.
    """
    endpoints = []
    try:
        methods_iter = _iter_methods(call_graph_snapshot)
        for node in methods_iter:
            fqn = node.get("id", "")
            name = node.get("name", "").lower()
            parent_class = str(node.get("parent_class", "") or "").lower()
            params = [str(p).lower() for p in (node.get("params") or [])]

            is_endpoint = False

            # Heuristic 1: method name keyword
            if any(kw in name for kw in _API_METHOD_KEYWORDS):
                is_endpoint = True

            # Heuristic 2: class name keyword
            if not is_endpoint:
                for kw in _API_CLASS_KEYWORDS:
                    if kw in parent_class:
                        is_endpoint = True
                        break

            # Heuristic 3: 'request' param
            if not is_endpoint:
                if any("request" in p for p in params):
                    is_endpoint = True

            if is_endpoint and fqn:
                endpoints.append(fqn)

    except Exception:
        pass

    return endpoints


# ---------------------------------------------------------------------------
# 2. find_affected_paths
# ---------------------------------------------------------------------------


def find_affected_paths(call_graph_snapshot, modified_files):
    """Find call paths that pass through any modified file.

    Args:
        call_graph_snapshot: A CallGraph instance whose compute_call_paths()
                             returns list of path dicts, or a dict with a
                             'call_paths' key.
        modified_files:      Iterable of file path strings (str or Path).

    Returns:
        list of path dicts (up to MAX_PATHS) where at least one node in
        path["path"] belongs to one of the modified files.
    """
    affected = []
    try:
        modified_set = _normalize_file_set(modified_files)
        if not modified_set:
            return affected

        paths = _get_call_paths(call_graph_snapshot)

        for path_dict in paths:
            if len(affected) >= MAX_PATHS:
                break
            node_fqns = path_dict.get("path", [])
            for fqn in node_fqns:
                if _fqn_belongs_to_files(fqn, modified_set):
                    affected.append(path_dict)
                    break

    except Exception:
        pass

    return affected


