def _fqn_to_module_and_class(fqn):
    """Split a FQN like 'path/module.py::ClassName.method' into parts.

    Returns:
        (module_import, class_name, method_name)
        module_import: dotted import path derived from file path
        class_name:    simple class name or None
        method_name:   simple method name
    """
    if "::" not in fqn:
        # Bare name with no file context
        return ("", None, fqn)

    file_part, rest = fqn.split("::", 1)

    # Convert file path to importable module dotted string
    module_import = _file_path_to_module(file_part)

    # rest may be 'ClassName.method' or just 'function_name'
    if "." in rest:
        parts = rest.rsplit(".", 1)
        class_name = parts[0]
        method_name = parts[1]
    else:
        class_name = None
        method_name = rest

    return module_import, class_name, method_name


def _file_path_to_module(file_path):
    """Convert a file path like 'langgraph_engine/foo.py' to
    an importable dotted module name 'langgraph_engine.foo'.

    Handles forward and backslashes.
    """
    p = file_path.replace("\\", "/").rstrip("/")
    # Strip leading './' if present
    if p.startswith("./"):
        p = p[2:]
    # Remove .py extension
    if p.endswith(".py"):
        p = p[:-3]
    # Replace separators with dots
    module = p.replace("/", ".")
    return module


def _safe_identifier(name):
    """Convert any string to a safe Python identifier fragment."""
    cleaned = re.sub(r"[^a-zA-Z0-9_]", "_", str(name))
    # Collapse repeated underscores
    cleaned = re.sub(r"_+", "_", cleaned)
    return cleaned.strip("_") or "item"


def _build_patch_target(module_import, class_name, method_name):
    """Build the dotted patch target for unittest.mock.patch."""
    if not module_import:
        if class_name:
            return "%s.%s" % (class_name, method_name)
        return method_name
    if class_name:
        return "%s.%s.%s" % (module_import, class_name, method_name)
    return "%s.%s" % (module_import, method_name)


def _default_test_input(params_list):
    """Generate a comma-separated placeholder arg string for the given params."""
    if not params_list:
        return ""
    args = []
    for p in params_list:
        pname = str(p).split(":")[0].strip()
        args.append("None  # TODO: %s" % pname)
    return ", ".join(args)


