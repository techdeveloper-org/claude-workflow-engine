# 4. generate_api_test
# ---------------------------------------------------------------------------


def generate_api_test(endpoint_method, framework="flask"):
    """Generate an API endpoint test stub for a single endpoint FQN.

    Args:
        endpoint_method: FQN of the endpoint method.
        framework:       One of 'flask', 'fastapi', 'django', 'spring',
                         or any other string (generic stub).

    Returns:
        str: Test method source code, or None if endpoint_method is empty.
    """
    if not endpoint_method:
        return None

    try:
        _, _, method_name = _fqn_to_module_and_class(endpoint_method)
        safe_name = _safe_identifier(method_name)
        fw = (framework or "").lower()

        if fw == "flask":
            return _FLASK_API_TEST.format(safe_name=safe_name, endpoint_method=endpoint_method)
        elif fw == "fastapi":
            return _FASTAPI_API_TEST.format(safe_name=safe_name, endpoint_method=endpoint_method)
        elif fw == "django":
            return _DJANGO_API_TEST.format(safe_name=safe_name, endpoint_method=endpoint_method)
        elif fw == "spring":
            return _SPRING_API_TEST.format(safe_name=safe_name, endpoint_method=endpoint_method)
        else:
            return _GENERIC_API_TEST.format(safe_name=safe_name, endpoint_method=endpoint_method)

    except Exception:
        return None


# ---------------------------------------------------------------------------
# 5. generate_integration_tests  (primary entry point)
# ---------------------------------------------------------------------------


def generate_integration_tests(project_root, modified_files, call_graph=None):
    """Generate integration tests based on call paths through modified files.

    Uses CallGraph.compute_call_paths() to find paths that pass through
    modified code. Each path becomes an integration test class.

    Args:
        project_root:   Root directory of the project (str or Path).
        modified_files: Iterable of file paths that were modified (str or Path).
        call_graph:     Optional CallGraph instance (or dict with 'call_paths').
                        When None the function returns an empty result dict.

    Returns:
        dict with keys:
            test_file_path  (str)  - where the integration test file should be written
            test_code       (str)  - generated pytest source code
            paths_tested    (int)  - number of call paths that generated tests
            methods_covered (int)  - total distinct method FQNs across all tested paths
    """
    empty = {
        "test_file_path": "",
        "test_code": "",
        "paths_tested": 0,
        "methods_covered": 0,
    }

    try:
        if call_graph is None:
            return empty

        root = Path(str(project_root))

        # Resolve affected call paths
        affected = find_affected_paths(call_graph, modified_files)
        if not affected:
            return empty

        # Build a methods lookup dict for param resolution
        methods_data = _build_methods_lookup(call_graph)

        # Determine output file path
        test_file_path = _resolve_test_file_path(root, modified_files)

        # Build test code
        code_parts = [_INT_FILE_HEADER]
        covered_fqns = set()

        for path_dict in affected:
            test_class = generate_path_test(path_dict, methods_data)
            if test_class and test_class.strip():
                code_parts.append(test_class)
                for fqn in path_dict.get("path", []):
                    covered_fqns.add(fqn)

        test_code = "\n".join(code_parts)

        return {
            "test_file_path": test_file_path,
            "test_code": test_code,
            "paths_tested": len(affected),
            "methods_covered": len(covered_fqns),
        }

    except Exception:
        return empty


